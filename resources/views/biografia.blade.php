      <h2>Biografía de Jorge Luis Borges</h2>
      {{-- <p>Jorge Luis Borges nace en Buenos Aires, 24 de agosto de 1899.Hijo de Jorge Guillermo Borges, abogado y profesor de psicología, y Leonor Acevedo Suárez. Borges Aprende a leer en inglés antes que en español por influencia de su abuela Frances Haslam, su abuela y de Miss Tink, una institutriz británica.</p>
      <p>Confiesa a partir de 1905 a su padre, el deseo de ser escritor, quien alienta su vocación. En los años siguientes redacta sus primeros textos: en inglés, un resumen de mitología griega, y en español, La visera fatal, cuento inspirado en un pasaje del Quijote; una tragedia para ser representada con su hermana, Bernardo del Carpio, y una traducción de El príncipe feliz de Oscar Wilde. En 1908 Ingresa en cuarto grado en una escuela primaria del Estado situada en la calle Thames.</p>
      <p>En 1912 Borges publica El rey de la selva, su primer cuento. Y dos años más tarde, en 1914 los Borges parten rumbo a Europa. Allí estudia en Ginebra e Inglaterra. Luego, durante 1919 y 1920 vive en España, se conecta con jóvenes poetas ultraístas y con escritores de la vanguardia española. En esos años colabora en revistas literarias, francesas y españolas, donde publica ensayos y manifiestos.</p>
      <p>En 1921, de regreso a Argentina, redescubre Buenos Aires, en especial los suburbios del sur, y comienza a escribir poemas que perfilan su obra posterior: el patio, los arrabales, las calles de extramuros, el malevo, el culto del coraje. Participa con Macedonio Fernández en la fundación de las revistas Prisma y Prosa y firma el primer manifiesto ultraísta en la revista Nosotros. En 1923 publica su primer libro de poemas, Fervor de Buenos Aires. Durante 1925 se asocia al grupo Sur y conoce a Victoria Ocampo, también ese año publica Luna de enfrente. </p>
      <p>Su primera operación de la vista ocurre en 1927, mejorando y alejando la sombra de la ceguera. </p>
      <p>Comenzando la década del 30, Borges se aleja de la poesía casi para dedicarse a la prosa. Conoce y se hace amigo de Adolfo Bioy Casares.</p>
      <p>En 1934, viaja por el norte del Uruguay, donde conoce la última frontera gaucha y presencia un asesinato en una pulpería. Muchos detalles de sus relatos provienen de este viaje. Continúa publicando en la Revista Multicolor de los sábados, de Crítica, (Historia universal de la infamia, y traducciones de Chesterton, Wells, Meyrink, Novalis y otros). Colabora en Sur y en Gaceta de Buenos Aires.</p>
      <p>Durante los años treinta publica diversas obras en colaboración con Adolfo Bioy Casares.  En estos años, su actividad literaria se amplía con la crítica literaria y la traducción de autores como Virginia Woolf, Henri Michaux o William Faulkner. Durante este período abandona las teorías del ultraísmo en busca de un camino estético personal.</p>
      <p>En 1935 Publica en un volumen Historia universal de la infamia, ya publicados en Crítica, y El hombre de la esquina rosada, cuento original y versión definitiva de Hombres pelearon. En 1936 aparece Historia de la eternidad (ensayos. Escribe la sección semanal Libros y autores extranjeros para la revista El Hogar. Colabora también en la revista Destiempo. Al año siguiente, en colaboración con el crítico dominicano Pedro Henríquez Ureña publica Antología clásica de la literatura argentina. Traduce el Orlando de Virginia Woolf para la editorial Sur. Comienza a trabajar como primer ayudante en la Biblioteca Municipal Miguel Cané, en el barrio de Boedo. Aprovecha allí el tiempo disponible para la lectura de La Divina Comedia, el Orlando de Ariosto y obras de León Bloy, entre otras lecturas.</p>
      <p>El 24 de febrero de 1938 muere su padre. El día de Nochebuena, Borges sufre un accidente que le produce septicemia, y casi le cuesta la vida. A partir de ese momento su visión disminuye y depende cada vez más de la ayuda de su madre y de varios amigos. Comienza a escribir narraciones fantásticas. Traduce y prologa La Metamorfosis de Kafka. Vive con su madre, su hermana Norah y su cuñado Guillermo de Torre.
        En 1940 irrumpe la publicación de la legendaria Antología de la literatura fantástica, en colaboración con Silvina Ocampo y Adolfo Bioy Casares.
        </p>
        <p>Es durante 1946 que, por haber firmado algunas declaraciones antiperonistas Borges es destituido de su puesto en la Biblioteca Municipal y se lo nombra inspector municipal de aves y conejos; renuncia sin hacerse cargo de este nombramiento. Comienza a dar conferencias y cursos en Buenos Aires, y más tarde en Uruguay y en las provincias argentinas. En colaboración con Adolfo Bioy Casares publica Un modelo para la muerte con el seudónimo de B. Suárez Lynch, y Dos fantasías memorables, firmada por H. Bustos Domecq.</p>
      <p>En 1949 aparece El Aleph. Durante 1950 dirige la cátedra de Literatura Inglesa en la Asociación Argentina de Cultura Inglesa y en el Colegio Libre de Estudios Superiores. Se interesa en el estudio de la literatura anglosajona medieval y despliega una intensa actividad como conferencista.</p>
      <p>El año 1953 es un año de expansión de su figura literaria. Comienza con Historia de la eternidad la edición de sus Obras Completas en Emecé, En París, Roger Caillois publica Labyrinths (traducción de algunos cuentos de El Aleph), y será uno de los difusores internacionales más destacados para su obra. Asimismo, aparece El Martín Fierro, uno de sus ensayos de mayor repercusión, escrito junto con Margarita Guerrero.</p>
      <p>En 1955 fue nombrado director de la Biblioteca Nacional de la República Argentina y profesor de literatura inglesa en la Universidad de Buenos Aires. A la edad de 55 años quedó casi completamente ciego.</p>
      <p>A partir de 1956 se le prohíbe leer y escribir por prescripción médica. Lentamente aprenderá a componer sus textos de memoria y a dictarlos a personas allegadas. </p>
      <p>Durante los años sesenta, su trabajo fue traducido y publicado en los Estados Unidos y en Europa. En 1961 llegó a la fama internacional al obtener el primer Premio Formentor, que recibió junto a Samuel Beckett. </p>
      <p>Luego de este premio, obtiene numerosas traducciones en otros idiomas. Se multiplican las invitaciones de otros países para tenerlo como orador en diferentes eventos culturales, así como también los premios nacionales e internacionales. Continúa escribiendo y publicando su obra.</p>
      <p>En 1985 viaja a Ginebra con su compañera María Kodama con quien se casa al año siguiente luego de una relación de muchos años. </p>
      <p>Jorge Luis Borges muere el 14 de junio en Ginebra (Suiza) Sus restos descansan en el cementerio de Plainpalais.</p> --}}

